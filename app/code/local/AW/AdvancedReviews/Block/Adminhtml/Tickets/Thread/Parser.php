/**
 * Parser Block converts "Review Id #id" to link for pleasure approve
 * reviews.
 */
class AW_AdvancedReviews_Block_Adminhtml_Tickets_Thread_Parser extends Mage_Adminhtml_Block_Template
{
    /**
     * Returns Url Pattern to Review Edit
     * @return String
     */
    public function getReviewUrl()
    {
        return $this->getUrl('adminhtml/catalog_product_review/edit', array('id'=>'{{review_id}}'));
    }
}
